static uint32_t TimingDelay;

/* Private function prototypes -----------------------------------------------*/

void initDelay()
{
    if (SysTick_Config(SystemCoreClock / 1000))
    { 
        /* Capture error */ 
    }
}
  
void Delay(uint32_t nTime)
{ 
  TimingDelay = nTime;

  while(TimingDelay != 0);
}

/**
  * @brief  Decrements the TimingDelay variable.
  * @param  None
  * @retval None
  */
void TimingDelay_Decrement(void)
{
  if (TimingDelay != 0)
  { 
    TimingDelay--;
  }
}